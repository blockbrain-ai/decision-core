function generateBaselinePolicy(profile: OnboardingProfile): string {
  const mode = profile.mode;
  const posture = profile.autonomy.posture;
  const action = toPolicyDecision(profile.autonomy.defaultAction);

  return `---
schema_version: "1.0.0"
policy_id: "baseline-${mode}"
title: "Baseline Policy — ${mode} mode"
description: "Auto-generated baseline policy for ${mode} setup"
effective_date: "${profile.createdAt}"
owner: "decision-core-setup"
surfaces:
  - workflow.escalation
---

# Baseline Policy

This policy was generated by Decision Core setup for **${mode}** mode with **${posture}** autonomy posture.

\`\`\`decision-core-clause
clause_id: baseline-default-action
clause_type: routing_constraint
surface_id: workflow.escalation
decision: "${action}"
safe_to_execute_without_model: true
rationale: "Default action for ${mode} mode with ${posture} posture"
\`\`\`

\`\`\`decision-core-clause
clause_id: baseline-unknown-action
clause_type: prohibition
surface_id: workflow.escalation
decision: "deny"
safe_to_execute_without_model: true
rationale: "Unknown actions are blocked by default"
\`\`\`
`;
}

function generateToolsPolicy(profile: OnboardingProfile): string {
  if (profile.tools.length === 0) return '';

  const clauses = profile.tools.map((tool) => {
    const surfaceId = surfaceForTool(tool);
    const action = decisionForTool(tool);
    const approvalRequired = tool.riskTier >= 3;
    const clauseType = tool.riskTier >= 4
      ? 'prohibition'
      : approvalRequired
        ? 'approval_requirement'
        : 'permission';

    return `\`\`\`decision-core-clause
clause_id: tool-${sanitizeId(tool.name)}
clause_type: ${clauseType}
surface_id: ${surfaceId}
decision: "${action}"
approval_required: ${approvalRequired}
safe_to_execute_without_model: true
rationale: "Risk tier ${tool.riskTier} tool${tool.canSpendMoney ? ' — can spend money' : ''}${tool.canDeleteData ? ' — can delete data' : ''}${tool.canDeployCode ? ' — can deploy code' : ''}"
\`\`\``;
  }).join('\n\n');

  const surfaces = [...new Set(profile.tools.map(surfaceForTool))];

  return `---
schema_version: "1.0.0"
policy_id: "tools-${profile.mode}"
title: "Tool Governance Policy"
description: "Auto-generated tool risk and approval rules"
effective_date: "${profile.createdAt}"
owner: "decision-core-setup"
surfaces:
${surfaces.map((s) => `  - ${s}`).join('\n')}
---

# Tool Governance

${clauses}
`;
}

function generateDataPolicy(profile: OnboardingProfile): string {
  if (profile.data.classes.length === 0) return '';

  const clauses = profile.data.classes.map((cls) => {
    const isSensitive = ['pii', 'financial', 'credentials', 'health', 'restricted'].includes(cls);

    return `\`\`\`decision-core-clause
clause_id: data-${cls}
clause_type: ${isSensitive ? 'obligation' : 'permission'}
surface_id: data.classification
decision: "${isSensitive ? 'approve_required' : 'allow'}"
safe_to_execute_without_model: true
rationale: "${cls} data classification${isSensitive ? ' — requires approval for access' : ''}"
\`\`\``;
  }).join('\n\n');

  return `---
schema_version: "1.0.0"
policy_id: "data-${profile.mode}"
title: "Data Handling Policy"
description: "Auto-generated data classification and handling rules"
effective_date: "${profile.createdAt}"
owner: "decision-core-setup"
surfaces:
  - data.classification
---

# Data Handling

${clauses}
`;
}

