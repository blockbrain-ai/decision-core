// ===========================================================================
// Helpers
// ===========================================================================

function sanitizeId(name: string): string {
  return name.replace(/[^a-zA-Z0-9_-]/g, '-').toLowerCase();
}

function sanitizeActionType(name: string): string {
  const sanitized = stripControlCharacters(name)
    .trim()
    .replace(/[^a-zA-Z0-9._-]/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '');
  return sanitized.length > 0 ? sanitized : 'tool';
}

function stripControlCharacters(value: string): string {
  let out = '';
  for (let i = 0; i < value.length; i += 1) {
    const code = value.charCodeAt(i);
    if (code > 0x1F && code !== 0x7F) out += value[i];
  }
  return out;
}

function toPolicyDecision(action: string): string {
  if (action === 'allow') return 'allow';
  if (action === 'ask') return 'approve_required';
  return 'deny';
}

function decisionForTool(tool: ProfileTool): string {
  if (tool.riskTier >= 4) return 'deny';
  if (tool.riskTier >= 3) return 'approve_required';
  return toPolicyDecision(tool.defaultAction);
}

function surfaceForTool(tool: ProfileTool): string {
  if (tool.canSpendMoney) return 'finance.processing';
  if (tool.canContactPeople || tool.canPublishContent) return 'communication.email';
  if (tool.accessesSensitiveData) return 'data.classification';
  if (tool.canDeleteData || tool.canDeployCode || tool.riskTier >= 3) return 'workflow.escalation';
  if (tool.defaultAction === 'ask') return 'workflow.escalation';
  return 'workflow.routing';
}

function simpleHash(content: string): string {
  let hash = 0;
  for (let i = 0; i < content.length; i++) {
    const char = content.charCodeAt(i);
    hash = ((hash << 5) - hash + char) | 0;
  }
  return Math.abs(hash).toString(16).padStart(8, '0');
}

