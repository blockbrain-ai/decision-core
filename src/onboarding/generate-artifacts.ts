function generatePolicyPackYaml(profile: OnboardingProfile): string {
  const rules: Array<Record<string, unknown>> = [];

  for (const tool of profile.tools) {
    const verdict = runtimeVerdictForTool(tool);
    const actionType = sanitizeActionType(tool.name);
    const entry: Record<string, unknown> = {
      name: `tool-${sanitizeId(tool.name)}`,
      description: `Policy for ${tool.name} (risk tier ${tool.riskTier})`,
      actionTypePattern: actionType,
      riskClass: tool.riskTier >= 3 ? 'A' : tool.riskTier >= 2 ? 'B' : 'C',
      enforcementPoint: 'pre_decision',
      policyType: tool.riskTier >= 3 ? 'safety' : 'business',
      priority: 50 + tool.riskTier * 10,
      requireApproval: verdict === 'approve_required',
      enabled: true,
    };
    if (verdict === 'deny') {
      entry['defaultVerdict'] = 'deny';
    }
    rules.push(entry);
  }

  if (profile.mode === 'team' || profile.mode === 'business') {
    rules.push({
      name: 'team-destructive-approval',
      description: 'Team/business mode: destructive operations require approval',
      actionTypePattern: 'delete_*',
      riskClass: 'B',
      enforcementPoint: 'pre_decision',
      policyType: 'safety',
      priority: 80,
      requireApproval: true,
      enabled: true,
    });
  }

  if (profile.mode === 'enterprise') {
    rules.push({
      name: 'enterprise-destructive-deny',
      description: 'Enterprise mode: destructive operations denied',
      actionTypePattern: 'delete_*',
      riskClass: 'A',
      enforcementPoint: 'pre_decision',
      policyType: 'safety',
      priority: 90,
      requireApproval: false,
      defaultVerdict: 'deny',
      enabled: true,
    });
    rules.push({
      name: 'enterprise-admin-approval',
      description: 'Enterprise mode: admin operations require approval',
      actionTypePattern: 'admin_*',
      riskClass: 'A',
      enforcementPoint: 'pre_decision',
      policyType: 'compliance',
      priority: 85,
      requireApproval: true,
      enabled: true,
    });
  }

  if (profile.tools.length === 0) {
    rules.push({
      name: 'starter-allow-read',
      description: 'Starter rule allowing read operations',
      actionTypePattern: 'read_*',
      riskClass: 'C',
      enforcementPoint: 'pre_decision',
      policyType: 'business',
      priority: 10,
      requireApproval: false,
      enabled: true,
    });
  }

  // Executive decisions (B2): the operator's owned high-risk capability calls
  // become TOP-priority rules so a deliberate allow/ask/block wins over tool tiers.
  const execPatterns: Record<string, string[]> = {
    delete_data: [
      'delete_*', 'delete-*', 'delete.*',
      'drop_*', 'drop-*', 'drop.*',
      'destroy_*', 'destroy-*', 'destroy.*',
      'purge_*', 'purge-*', 'purge.*',
    ],
    move_money: [
      'payment_*', 'payment-*', 'payment.*',
      'transfer_*', 'transfer-*', 'transfer.*',
      'refund_*', 'refund-*', 'refund.*',
      'charge_*', 'charge-*', 'charge.*',
    ],
    deploy: [
      'deploy_*', 'deploy-*', 'deploy.*',
      'release_*', 'release-*', 'release.*',
      'rollout_*', 'rollout-*', 'rollout.*',
    ],
    external_contact: [
      'send_*', 'send-*', 'send.*',
      'email_*', 'email-*', 'email.*',
      'contact_*', 'contact-*', 'contact.*',
      'publish_*', 'publish-*', 'publish.*',
    ],
    credentials: [
      'credential_*', 'credential-*', 'credential.*',
      'secret_*', 'secret-*', 'secret.*',
      'token_*', 'token-*', 'token.*',
    ],
  };
  for (const [cap, decision] of Object.entries(profile.autonomy.executiveDecisions ?? defaultExecutiveDecisions())) {
    for (const pattern of execPatterns[cap] ?? []) {
      const entry: Record<string, unknown> = {
        name: `exec-${cap}-${sanitizeId(pattern)}`,
        description: `Executive decision: ${cap} -> ${decision}`,
        actionTypePattern: pattern,
        riskClass: 'A',
        enforcementPoint: 'pre_decision',
        policyType: 'safety',
        priority: 95,
        requireApproval: decision === 'ask',
        enabled: true,
      };
      if (decision === 'block') entry['defaultVerdict'] = 'deny';
      if (decision === 'allow') entry['defaultVerdict'] = 'allow';
      rules.push(entry);
    }
  }

  return stringifyYaml({
    version: '1.0.0',
    name: `${profile.mode}-setup`,
    description: `Generated by decision-core setup for ${profile.mode} mode`,
    denyUnknownDefault: true,
    rules,
  });
}

function runtimeVerdictForTool(tool: ProfileTool): 'allow' | 'deny' | 'approve_required' {
  if (tool.riskTier >= 4) return 'deny';
  if (tool.riskTier >= 3) return 'approve_required';
  if (tool.defaultAction === 'block') return 'deny';
  if (tool.defaultAction === 'ask') return 'approve_required';
  return 'allow';
}

export function generateRootConfigYaml(
  profile: OnboardingProfile,
  policyPackRelPath: string,
  opts?: { observationStorePath?: string },
): string {
  // The rules are REAL (denyUnknownDefault stays true); enforcementMode is what
  // makes a fresh install non-breaking. In 'observe' nothing is blocked — the
  // would-be denials are recorded so the operator can review before flipping to
  // 'enforce' (a one-line change here; no pack regeneration needed).
  //
  // In observe mode we PERSIST the decision log (SQLite) when the caller supplies
  // a store path, so the shadowed denials survive restarts and are reviewable via
  // `decision-core observations`. This is an onboarding artifact only — pure
  // SDK/library callers keep the in-memory default unless they opt in. The caller
  // (setup) gates the path on SQLite availability and falls back to memory + a
  // warning rather than silently losing observations.
  const observing = profile.autonomy.enforcementMode === 'observe';
  const persistObservations = observing && !!opts?.observationStorePath;

  const config: Record<string, unknown> = {
    tenantId: 'default',
    persistence: persistObservations ? 'sqlite' : 'memory',
    tenantMode: 'single',
    policyPackPath: policyPackRelPath,
    denyUnknownDefault: true,
    enforcementMode: profile.autonomy.enforcementMode,
  };
  if (persistObservations) config.sqlitePath = opts!.observationStorePath;

  const observeHeader = observing
    ? `# OBSERVE MODE: non-blocking — records would-be denials${persistObservations ? ` to ${opts!.observationStorePath} (gitignored)` : ' (in-memory only — install better-sqlite3 to persist)'}.
#   Review what would have been blocked:  decision-core observations
#   Flip to real enforcement:             decision-core enforce
`
    : '';

  return `# decision-core.yaml — root runtime config
# Generated by decision-core setup
${observeHeader}
${stringifyYaml(config)}`;
}

