function generateSurfaceContracts(profile: OnboardingProfile): string {
  if (profile.surfaces.length === 0 && profile.tools.length === 0) {
    return `# Surface Contracts
# No surfaces or tools configured
surfaces: []
`;
  }

  const toolsByRisk: Record<string, ProfileTool[]> = {};
  for (const tool of profile.tools) {
    const key = tool.riskTier >= 3 ? 'high_risk' : tool.riskTier >= 2 ? 'medium_risk' : 'standard';
    if (!toolsByRisk[key]) toolsByRisk[key] = [];
    toolsByRisk[key].push(tool);
  }

  const surfaces: Array<{ name: string; riskClass: 'A' | 'B' | 'C'; tools: string[] }> = [];

  if (toolsByRisk['high_risk']?.length) {
    surfaces.push({
      name: 'high-risk-operations',
      riskClass: 'A',
      tools: toolsByRisk['high_risk'].map((t) => t.name),
    });
  }

  if (toolsByRisk['medium_risk']?.length) {
    surfaces.push({
      name: 'standard-operations',
      riskClass: 'B',
      tools: toolsByRisk['medium_risk'].map((t) => t.name),
    });
  }

  if (toolsByRisk['standard']?.length) {
    surfaces.push({
      name: 'low-risk-operations',
      riskClass: 'C',
      tools: toolsByRisk['standard'].map((t) => t.name),
    });
  }

  return `# Surface Contracts\n# Generated at: ${profile.createdAt}\n\n${stringifyYaml({ surfaces })}`;
}

