function generateProfileYaml(profile: OnboardingProfile): string {
  return `# Decision Core Profile
# Generated at: ${profile.createdAt}

schemaVersion: 1
profileId: "${profile.profileId}"
mode: "${profile.mode}"

agent:
  harness: "${profile.agent.harness}"
  detectedTools: ${JSON.stringify(profile.agent.detectedTools)}

autonomy:
  posture: "${profile.autonomy.posture}"
  defaultAction: "${profile.autonomy.defaultAction}"
  alwaysRequireApproval: ${JSON.stringify(profile.autonomy.alwaysRequireApproval)}

provider:
  mode: "${profile.provider.mode}"

memory:
  primarySource: ${profile.memory.primarySource ? `"${profile.memory.primarySource}"` : 'null'}
  evidenceImported: ${profile.memory.evidenceImported}
`;
}

function generateConfigYaml(profile: OnboardingProfile): string {
  return `# Decision Core Configuration
# Generated at: ${profile.createdAt}

version: 1
mode: "${profile.mode}"
provider:
  mode: "${profile.provider.mode}"
${profile.provider.envVarName ? `  envVarName: "${profile.provider.envVarName}"` : ''}
${profile.provider.localEndpoint ? `  localEndpoint: "${profile.provider.localEndpoint}"` : ''}
autonomy:
  posture: "${profile.autonomy.posture}"
  defaultAction: "${profile.autonomy.defaultAction}"
  enforcementMode: "${profile.autonomy.enforcementMode}"
`;
}

