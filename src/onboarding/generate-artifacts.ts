export function generateArtifacts(profile: OnboardingProfile): GenerationResult {
  const validation = OnboardingProfileSchema.safeParse(profile);
  if (!validation.success) {
    return {
      artifacts: [],
      profileHash: '',
      generatedAt: new Date().toISOString(),
      warnings: [`Profile validation failed: ${validation.error.issues.map((i) => i.message).join(', ')}`],
    };
  }

  const warnings: string[] = [];
  const artifacts: GeneratedArtifact[] = [];

  artifacts.push({
    path: 'decision-core.profile.yaml',
    content: generateProfileYaml(profile),
    category: 'profile',
  });

  artifacts.push({
    path: 'decision-core.config.yaml',
    content: generateConfigYaml(profile),
    category: 'config',
  });

  artifacts.push({
    path: 'surface-contracts.yaml',
    content: generateSurfaceContracts(profile),
    category: 'surface',
  });

  const baseline = generateBaselinePolicy(profile);
  artifacts.push({ path: 'policies/000-baseline.md', content: baseline, category: 'policy' });

  const toolsPolicy = generateToolsPolicy(profile);
  if (toolsPolicy) {
    artifacts.push({ path: 'policies/010-tools.md', content: toolsPolicy, category: 'policy' });
  } else {
    warnings.push('No tools configured — skipping tool policy generation');
  }

  const dataPolicy = generateDataPolicy(profile);
  if (dataPolicy) {
    artifacts.push({ path: 'policies/020-data.md', content: dataPolicy, category: 'policy' });
  }

  artifacts.push({
    path: 'policies/030-provider-routing.md',
    content: generateProviderPolicy(profile),
    category: 'policy',
  });

  const memoryPolicy = generateMemorySourcePolicy(profile);
  if (memoryPolicy) {
    artifacts.push({ path: 'policies/040-memory-sources.md', content: memoryPolicy, category: 'policy' });
  }

  artifacts.push({
    path: 'policy-pack.yaml',
    content: generatePolicyPackYaml(profile),
    category: 'config',
  });

  artifacts.push({
    path: 'tests/generated-scenarios.json',
    content: generateTestScenarios(profile),
    category: 'test',
  });

  artifacts.push(generateHarnessIntegration(profile));

  const report = generateOnboardingReport(profile, warnings);
  artifacts.push({ path: 'reports/onboarding-report.md', content: report, category: 'report' });

  artifacts.push({
    path: 'memory-source-report.md',
    content: generateMemorySourceReport(profile),
    category: 'report',
  });

  artifacts.push({
    path: 'rollback-manifest.json',
    content: generateRollbackManifest(artifacts, profile.createdAt),
    category: 'config',
  });

  const profileHash = simpleHash(JSON.stringify(profile));

  return {
    artifacts,
    profileHash,
    generatedAt: new Date().toISOString(),
    warnings,
  };
}
