/**
 * Structured Artifact Generator
 *
 * Takes a confirmed OnboardingProfile and generates the full .decision-core/
 * directory: profile, config, policies, surface contracts, tests, reports.
 */

import type { OnboardingProfile, ProfileTool } from '../contracts/onboarding-profile.contracts.js';
import { OnboardingProfileSchema, defaultExecutiveDecisions } from '../contracts/onboarding-profile.contracts.js';
import { stringify as stringifyYaml } from 'yaml';

// ===========================================================================
// Output Types
// ===========================================================================

export interface GeneratedArtifact {
  path: string;
  content: string;
  category: 'profile' | 'config' | 'policy' | 'surface' | 'test' | 'integration' | 'report';
}

export interface GenerationResult {
  artifacts: GeneratedArtifact[];
  profileHash: string;
  generatedAt: string;
  warnings: string[];
}

