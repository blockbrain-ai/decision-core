function generateMemorySourcePolicy(profile: OnboardingProfile): string {
  const consented = profile.memory.sources.filter((s) => s.readConsent);
  if (consented.length === 0) return '';

  const clauses = consented.map((s) => {
    return `\`\`\`decision-core-clause
clause_id: memory-${sanitizeId(s.kind)}
clause_type: permission
surface_id: data.extraction
decision: "allow"
safe_to_execute_without_model: true
rationale: "Memory source ${s.kind} — read consent granted${s.writeBackConsent ? ', write-back consent granted' : ', read-only'}"
\`\`\``;
  }).join('\n\n');

  return `---
schema_version: "1.0.0"
policy_id: "memory-sources"
title: "Memory Source Policy"
description: "Documents consented memory sources for onboarding"
effective_date: "${profile.createdAt}"
owner: "decision-core-setup"
surfaces:
  - data.extraction
---

# Memory Sources

${clauses}
`;
}

